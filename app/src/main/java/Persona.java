public class Persona {
    private String id;
    private String nombre;

    public void setId(String id) {
        this.id = id;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getId() {
        return id;
    }
    public String getNombre() {
        return nombre;
    }
}
